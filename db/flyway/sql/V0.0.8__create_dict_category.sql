    "DICT_CATEGORY_ID" VARCHAR2(36) NOT NULL ENABLE,
    "NAME" VARCHAR2(255),
    "CODE" VARCHAR2(255),
    "PARENT" VARCHAR2(36),
    "IS_LEAF" NUMBER(1, 0),
    "DESCRIPTION" VARCHAR2(255),
    "CREATE_TIME" DATE,
    "CREATE_USER_ID" VARCHAR2(36),
    "MODIFY_USER_ID" VARCHAR2(36),
