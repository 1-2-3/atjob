COMMENT ON COLUMN "TPL_DICT_CATEGORY"."CREATE_TIME" IS '创建时间';

COMMENT ON COLUMN "TPL_DICT_CATEGORY"."CREATE_USER_ID" IS '创建人Id@AUTH_USER';

